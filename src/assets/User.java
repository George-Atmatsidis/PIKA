package assets;

public class User {
	private String name;
	private String address;
	private String telephone;
	private Integer age;
	private Integer status;
	public User(String name, String address, String telephone, Integer age, Integer status) {
		super();
		this.name = name;
		this.address = address;
		this.telephone = telephone;
		this.age = age;
		this.status = status;
	}
	
	
}
